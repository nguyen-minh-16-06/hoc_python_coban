#Dictionary là kiểu dữ liệu cho phép ta thay đổi các phần tử.
''' Hướng dẫn:
Để thêm một phần tử mới thì ta sử dụng toán tử gán '=':
- Nếu phần tử chưa tồn tại thì dict sẽ tự bổ sung nó vào
- Nếu đã tồn tại thì nó sẽ được cập nhật giá trị '''

dict = {'name':'MINH', 'age': 18}
dict['age'] = 20
print(dict)

# Thêm vào dict phần tử:
dict['address'] = '94 NGUYEN HUE'
print(dict)
