''' HÀM SẮP XẾP - SORTED '''
''' 
	Syntax: 
		   sorted(iterable, /, *, key=None, reverse=False)
		   						  					 	  '''

# Công dụng: Giống với phương thức sort của List object.

itera = [1, 16, 6, 5]
print(itera)
print(sorted(itera, reverse = True))
# Nếu reverse = False thì sẽ sắp xếp từ bé đến lớn & ngược lại.

