''' SEP (SEPARATE - CHIA RA, PHÂN RA) '''

# Giá trị mặc định của parameter này là một khoảng trắng.
# Khi các argument bạn cho vào hàm print để hàm print in ra nội dung,
# như đã biết là nó sẽ được gói vào một Tuple.
# Các giá trị trong Tuple sẽ được nối với nhau bằng parameter sep (khoảng trắng)

# Mặc định bình thường không có sep thì mọi phần tử nối với nhau bằng 1 khoảng trắng
print('LE', 'NGUYEN', 'MINH')

# Khi có sep thì mọi phần tử nối với nhau bằng 3 dấu '---'
print('LE', 'NGUYEN', 'MINH', sep = '---')