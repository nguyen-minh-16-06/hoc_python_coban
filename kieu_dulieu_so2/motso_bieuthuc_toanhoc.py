x = 10
y = 10

#tổng của X với Y
c = x + y
print(c)

#hiệu của X với Y
d = x - y
print(d)

#Tích của X với Y
s = x * y
print(s)

#Thương của X với Y
f = x / y
print(f)

#Thương nguyên của X với Y (kết quả luôn lớn hơn hoặc bằng X/Y)
z = x // y
print(z)

#dư của thương X với Y
q = x % y
print(q)

#luỹ thừa X với cơ số Y
r = x ** y
print(r)