'''Toán tử logic trong python'''
# Toán tử logic là những loại toán tử
# Được dùng để kiểm tra sự liên kết giữa hai biểu thức
# Kết quả nó trả về là True hoặc False
A = True
B = False

# A and B: Trả về True nếu cả A và B có giá trị True, ngược lại trả về False
print (A and B) # False

# A or B: Trả về True nếu một trong hai A hoặc B có giá trị True, ngược lại trả về False
print (A or B) # True

''' CÁI NÀY KHÔNG CẦN THIẾT LẮM '''
# not(A): Trả về True nếu A là False, và trả về False nếu A là True.
# Đây là toán tử phủ định
print (not A) # False
